+--------------------------+---------------------------------------------------------+-------------------------------------------+
|  NOT LIKE ...            |  Case insensitive exact string inequality comparison    |  col_name NOT LIKE "string_name"          |
+--------------------------+---------------------------------------------------------+-------------------------------------------+
|  LIMIT ...               |  Limits the number of expected outcomes                 |  LIMIT value                              |
+--------------------------+---------------------------------------------------------+-------------------------------------------+
|  OFFSET ...              |  Skips the first specified set of outcomes              |  OFFSET value                             |
